    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        
        StringBuilder sb = new StringBuilder();
    
    if (txtusername.getText().equals("") || new String(txtpassword.getPassword()).equals("")) {
        JOptionPane.showMessageDialog(this, "Thông tin tài khoản hoặc mật khẩu không đủ!");
        return;
    }
    
    try {
        String username = txtusername.getText();
        String password = new String(txtpassword.getPassword());
        loginModify loginMod = new loginModify();
        ModelTaiKhoan tk = loginMod.login(username, password);
        
        if (tk != null) {
            frmTrangChu TrangChu = new frmTrangChu();
            TrangChu.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Thông tin tài khoản hoặc mật khẩu không đúng!");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnLoginActionPerformed

